def find_kext(directory, kext):
    re_pattern = re.compile(rf"{kext}$", re.IGNORECASE)
    for root, _, files in os.walk(directory):
        for file in files:
            file_path = os.path.join(root, file)
            if re_pattern.search(file_path):
                return file_path
    return None


